Map::Map(std::string path) {
    std::ifstream file = std::ifstream(path);

    int i = 0;
    if(file.is_open()) {
        printf("Gamer");
        std::string line;
        while(getline(file, line)) {
            this->worldMap.push_back(std::vector<char>());
            for(unsigned long j = 0; j < line.size(); j++) {
                this->worldMap[i].push_back(line.c_str()[j]);
                this->width = line.size();
            }
            this->height += 1;
        }
    } else {
        printf("could not open map: %s\n", path.c_str());
    }
}
